package com.greencake.domain;

public class Mascot {
	private int mascotid;
	private String mascottype;
	private String mascotluckycontent;
	private String generalnotes;

	public int getMascotid() {
		return mascotid;
	}

	public void setMascotid(int mascotid) {
		this.mascotid = mascotid;
	}

	public String getMascottype() {
		return mascottype;
	}

	public void setMascottype(String mascottype) {
		this.mascottype = mascottype;
	}

	public String getMascotluckycontent() {
		return mascotluckycontent;
	}

	public void setMascotluckycontent(String mascotluckycontent) {
		this.mascotluckycontent = mascotluckycontent;
	}

	public String getGeneralnotes() {
		return generalnotes;
	}

	public void setGeneralnotes(String generalnotes) {
		this.generalnotes = generalnotes;
	}

}
